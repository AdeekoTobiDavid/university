Faculty of Agricultural Science <br />
History<br /><br /> 

A proposal for the establishment of the Faculty of Agricultural Sciences (FAS) was presented to the Senate by the immediate past Vice-Chancellor, Professor V. A. Tenebe at its 68th regular meeting held on Tuesday, 24th January, 2012. A committee was set up under the chairmanship of the Dean, School of Post-Graduate Studies, Professor Israel F. Adu, who is an agriculturist.<br /><br />

At the 69th meeting of the Senate held on Tuesday, 24th April, 2012, the report and approval for the establishment of the Faculty of Agricultural Sciences from the existing Faculty of Science and Technology was ratified.  The Faculty officially took off in July 2014 with Prof. Matthew Dada Alegbejo as the pioneer Dean.  Professor Nda E. Mundi is currently the Dean, School of Agricultural Sciences effective from June 1st 2015.<br /><br />
Vision<br /><br />

To be regarded as the foremost Faculty, providing highly accessible and enhanced quality education in all areas of agriculture through Open and Distance Learning (ODL) and graduates suitable for the development of the society through teaching, learning and research.<br />
Mission<br /><br />

To provide functional, cost-effective and flexible agricultural learning required for efficient agricultural production, self employment and sustainable development in various aspects of agriculture.<br />
Aims and Objectives<br /><br />

The Faculty of Agricultural Sciences aims to provide:<br /><br />

    Training opportunity for sustainable bio-resource management and entrepreneurship in agricultural production.<br />
    Opportunity for vocational training and professional workshops for retooling/reskilling in different areas of agriculture.<br />
    Relevant supportive resources for teaching and research in agriculture and allied disciplines through the Open and Distance Learning (ODL) mode of delivery.<br />
    Opportunity for specialization and advance study in major disciplines of agriculture.<br />
    Enhanced agricultural production through community-based and farmer-driven research. <br /><br /><br />

 
General Information<br /><br /><br />

 
Graduation Requirements<br /><br />

To graduate, a student shall have undergone 4 or 5 years of study depending on his entry point, including 12 calendar months of Farm Practical Year (FPY/SIWES).  The activities of the farm practical year should include periodic seminars on the studentâ€™s work as a way of stimulating interest as well as the presentation of a written report to be graded at the end of the year.<br /><br />

Course workload must meet the graduation requirements of the university.  However, in doing so, the student must earn a minimum of 167 credit units for the five-year programme and 132 credit units for the four-year (direct entry) programme in agriculture and related fields as indicated under course requirements.<br /><br />

The submission of an undergraduate project report based on supervised research is a graduation requirement, which must not be compromised.  This requirement exposes the student to problem-solving techniques and provides him with the ability to organize ideas from literature and research findings.  In short, it prepares the student for the work ahead and for further training at the post-graduate level.  This area of academic preparation needs to be maintained and developed further.<br /><br />
