Instructional Modes<br /><br />

The method of instruction at NOUN is by the distance learning  mode within an open learning environment. For clarity, open learning is defined as "any form  of learning  in which  the process enables individual learners to exercise choice over any one or more of a number  of aspects of learning." Furthermore, distance education is defined as "an educational process in which a significant proportion of the teaching is conducted by someone removed in space and/or time from the learners."<br /><br />

Distance education involves the use of a range of media such as print, written correspondence, audio, video, computer-based media and networks as well as multimedia facilities for  the presentation of information and for communication between the university and her students.<br /><br /><br /><br />

instructional mode

     This instructional mode provides the opportunity for working people  to  acquire  knowledge, skills  and techniques which may be relevant to their present work situation  or to future  career prospects. Distance education thus provides people with the opportunity to  improve  their  academic qualification without giving up their jobs and without being removed  from  their domicile  areas.<br />
	 The system also allows for teaching people who are living in any location, regardless of the types of jobs they do. The system is also flexible in terms of age and time allowing for enrolment at any time  and age and at a convenience of pace dictated by the student.<br />
Instructional Delivery<br /><br />

The NOUN instructional delivery system is modelled after that of Indira Ghandi National Open University (IGNOU), India. NOUN does not provide lectures to students in the normal classroom situation. Instead, it teaches through the provision of specialised study materials on the basis of self learning. These study materials are given or sent to students who are enrolled for the courses by the postal service. The teaching is provided through the use of the following:<br />
Printed Materials<br /><br />

Printed materials will remain a major instructional mode until such a time that the infrastructure for total online teaching will become available. Each course has a study material written specifically for the course and which students are expected to study prior to being examined.<br />
Audio Tapes, Videotapes<br /><br />

In addition to printed materials, some of the courses are recorded on audio/video tapes to be played as often as possible and in whatever environment is convenient for the play backs.<br />
CO-ROMs<br /><br />

Complimentary to the print materials is the CD-ROM which constitutes one of the wonders of ICT. It affords the opportunity of storing a vast amount of information and carrying this about. CO-ROMs could be both audio and visual.<br />

