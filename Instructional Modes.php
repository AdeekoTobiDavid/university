Radio and Television Broadcast<br /><br />

Some courses will be broadcast on the radio and some on television. This is usually well organized and arranged by a time table which will be widely publicized for all the students concerned. In addition, Public enlightenment programmes will be broadcast on radio and television for advocacy.<br />
Computer-mediated Learning<br /><br />

Recent innovations in the field of communication technology are being exploited to make the delivery of instruction even more effective. As the country becomes more accessible via the internet and more students get access to personal computers, learning at a distance has become more enabling. Through the NOUN i-Learn programme, students now engage in a whole range of interactions with their peers and tutors.<br /><br />

 










			
