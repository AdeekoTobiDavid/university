Welcome to the Information Gateway - the E-Library of the National Open University of Nigeria.<br/><br />

The Information Gateway is the online library of the National Open University of Nigeria. It is the gateway to a wide range of information resources that will help you in your studies, your work, your research, and in your general development and pursuit of scholarship. Each resource provides very helpful tools to assist you navigate through the contents. Multimedia Tutorials for each information resource is being prepared to enrich your experience in the use of our electronic library.<br/><br />

THE NOUN Library is very young. It is just being established to support teaching, learning, research etc activities of the university. Its major aim is to facilitate access to scholarly information resources. The main Library is located at the headquarters of the National Open University of Nigeria in Lagos. The Library provides Reference, Information Literacy, Circulation, Current Awareness Services among others. Internet access through local area networks and wireless connectivity is running 24/7. The E-Library laboratory is equipped with 45 state-of-the-art high speed computers.<br/><br /><br/><br />

 

The library building is being renovated to provide services to users who prefer print copies of books and periodicals. Registered students are welcomed to use our rich resources, surf the net, consult our journals and other periodicals.<br/><br />

The Library's collection is expected to grow to 20,000 volumes of books and 100,000 volumes of electronic resources. It also has a growing periodical and audio-visual collection. Our classification scheme remains the popular Library of Congress Classification and Subject Headings common among university libraries in Nigeria. You are welcome to forage the growing e-resources of the Library. The University Library is pursuing vigorously the new Millennium integrated library system which will place us first among equals in our sub-region. Welcome to the Information Gateway<br/><br />

