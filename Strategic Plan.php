Strategic Plan<br/><br />

The concept and practice of Open and Distance Learning {ODL) in Nigeria has taken new dimensions within the last decade, especially with the resuscitation of the National Open University of Nigeria (NOUN). This calls for a strategic plan that entails a complete paradigm shift from the traditional mode of learning to a combination of face-to-face, learner-centred,technology-assisted, interactive, just­ in-time learning, with no social bias or hindrances. The NOUN plan proposes to develop in five major areas.They are to:<br/><br />

    Enhance student enrolment in programmes being offered in the university<br />
    Create demand-driven academic programmes relevant to the needs of the society<br />
    Create linkages with other universities and industrial concerns<br />
    Achieve staff competencies in ODL<br />
    Deploy the use of innovative technologies in the delivery of her operational services<br /><br /><br />

Our vision is that when the plan is successfully carried out, NOUN will be able to justify its leading role as a leading ODL centre in the world.<br /><br />

For our learners, this means an opportunity to experience a flexible, interactive and student-centred learning environment, of a well coordinated and integrated set of instructions that will lead to awards of certificates, diplomas, degrees and acquisition of competencies that will equip them to face the world with confidence.<br /><br />

For staff, the vision means their competencies will be empowered via provision of Methodologies that will accommodate the integration of the various delivery methods available for modern learning in an ODL setting.<br /><br />

For the university as a whole, the vision implies improved administrative efficiency and service, greater expansion and collaborative opportunities, and making the university an exemplar in the demonstration of the deployment of ODL to knowledge and skill acquisition in all its programmes and activities.<br />
Expectations for Clients<br /><br />

Certain expectations are required of NOUN as it offers its services and products. These are:  hitch free admission for all candidates into the appropriate programmes  of the university  according to individual qualification and/or professional standing and experience;attainment of delivery target of service delivery within one week of the respective target period; prompt response to enquiries from learners through the respective organs of the university such as the caB centre office,public affairs,e-mail and internet;and not the least, producing  well-grounded graduates found fit in learning and character for self-realisation  and the labour market.<br />
Implementation Strategy<br /><br />

NOUN would seek collaboration with professional bodies/organisations to develop learning materials and produce instructionally well-designed course materials as study packs for students. Due to the special features of ODL, NOUN would have to ensure provision  of learner  support  services through  the use of study centres at various  locations  in the country;  employ  competent  instructional facilitators  to  impart instructions  at these study centres; and use student  group  learning  activities,  presentations,  etc. to enhance  learning  at  the  various  study  centres, and  encourage  the  use of  appropriate multimedia technology to achieve delivery of instructions.  In order to attract robust participation of the stakeholders, part of the strategy would be to be conducting workshops, seminars, short-term courses, conferences for groups and individuals at educational institutions and various workplaces and to engage in strong advocacy.<br />
Strategic and Operational Priorities<br /><br />

The immediate priority is maintaining the provision of instructional materials to the students in any of the delivery formats at the respective study centres and the continuing re-appraisal of the relevance of the various programmes to meet prevailing demands of the society. To achieve this operational strategy, the reward system will accommodate increased responsibility for staff. The university will also improve the work environment for its staff and provide them with modern and effective working tools to enable them achieve job actualisation and satisfaction.<br />
Strategic Direction<br /><br />

Our strategic direction is to implement the following:<br /><br />

    Offer new and locally relevant  programmes,  attain  full development of the ICT network within and amongst the NOUN systems, and provide the best possible learning experiences for our students through the use of a range of contextually relevant media and technologies;<br />
    Build upon the e-learning initiatives  already begun with  a gradual expansion to full delivery  courses online by the end of the Plan period;<br />
    Encourage increased use of student support services through the completion of ongoing renovations and refurbishment of the first phase of study centres while the second stage of study centres' development to include all states and selected study centres reflecting concentration of students will be embarked upon;<br />
    Lay the foundation for quality research and the identification of research priorities essential for a new and growing open university; exhibit excellence in the design, development and provision of instructional materials and tutorial facilities to be comparable to any ODL institutions anywhere in the world.<br />

Strategic Aims<br /><br />

To collaborate  with  institutions and organisations  within  the  West African sub-region  for training  and development  in ODL through  the activities  of RETRIDAL; and enhance the growth  and development  of NOUN and its  staff  through  partnerships  with  international development partners  and  professional organisations and other  open and distance learning  institutions worldwide such as Commonwealth  of Learning  (COL),<br />
 United  Nations  Educational,  Scientific   and  Cultural  Organisation   (UNESCO), New Partnership for Africa's Development  (NEPAD), West African Distance Educators (WADE), African Council for Distance Education (ACDE), International Council for Open and Distance Education (ICDE),University of South Africa (UNISA), Open University of United  Kingdom (OUUK), University  of Southern  Queensland (USQ),Open University of Hong Kong (OUHK) Indira Ghandi National Open University (IGNOU),to mention a few.<br /><br />

To develop instructional materials suitable for international students beginning with the West African sub-region through the African region and to the rest of the world in keeping with the trend·in cross border education.<br /><br /><br />

 





















			
