Welcome to the School of Postgraduate Studies of the National Open University of Nigeria, (NOUN) the only Nigerian University dedicated Distance Learning in the Country. Thank you for choosing the NOUN to pursue your postgraduate studies that will have profound influence on your future.<br />
The National Open University of Nigeria from the inception of its academic programmes in 2003, had commenced Postgraduate studies in each of its five schools particularly at the postgraduate Diploma level. Nonetheless, the Senate of the NOUN formalised the establishment of a School of Postgraduate Studies at its 46th Regular Meeting of April 6, 2009.<br />
The Postgraduate programmes of the University are run either on Full-time (particularly for research-based Ph.D and Masters Degrees) OR flexible time bases. Our programmes are designed to expand the depth of your knowledge and to develop new skills, update your professional skills as well as add value to your organisation and business. Hence most of our programmes, particularly at the Postgraduate Diploma and Masters level, are by Coursework and Research underpinned by the principles of Open and Distance Learning.<br /><br /><br />
 
Prof Mande SamailaProf. Mande Samaila<br /><br /><br />

SAMAILA MANDE is a scholar and Professor of Marketing & Communication. Presently, he is the Dean of the School of Postgraduate Studies and Coordinator of the NOUN Business School at the National Open University of Nigeria. He was an Adjunct Research Fellow at the School of Media and Communication, Pan-Atlantic University, Lagos State. He was the National Secretary of the Academy of Management Nigeria (TAMN). A Faculty Member of the Nigerian Military School of Public Relations, Bonny Cantonment, Lagos; and also a Faculty Member of both the Nigerian Military School of Medical Sciences, Ojo Cantonment, Lagos and the Nigerian Army War College, Abuja.<br /><br />

He was the pioneer Head of the Department of Mass Communication, Igbinedion University, Okada, where he also served as the Sub-Dean, College of Arts and Social Sciences. He was also a Visiting Lecturer in the Department of Mass Communication at Kogi State University, Ayingba, Kogi State and Benson Idahosa University, Benin City, Edo State, Nigeria. He earlier worked with Newswatch Communications Limited.<br /><br />

He has membership of professional bodies such as: National Institute of Marketing of Nigeria (FNIMN); Nigeria Institute of Public Relations (FNIPR); Nigerian Institute of Management (MNIM); Advertising Practitioners Council of Nigeria (APCON); Institute of Management Consultants (IMC) and an accredited Management Consultant of the Centre for Management Development (CMD); The African Council for Communication Education (ACCE) and The Academy of Management Nigeria (TAMN). He is currently the President of The Brands Academy Nigeria (BRAN). He is also concurrently a member of the Governing Councils of the nationâ€™s premier University, the University of Ibadan and the Governing Council of the National Open University of Nigeria (NOUN), Abuja.<br /><br />

