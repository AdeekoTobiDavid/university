Admission Requirements<br /><br />

    To qualify for admission into any of the postgraduate programmes of the University, a candidate must meet, among others, all matriculation requirements of at least five (5) credit passes including English Language and Mathematics. Some programmes accept at least a pass in Mathematics.<br />
    For Postgraduate Diploma Programmes, Candidates must possess either a minimum of pass Bachelors Degree in any discipline from a University recognised by the Senate of the NOUN OR a Higher National Diploma with a minimum of Lower Credit. For the Postgraduate Diploma in Legislative Drafting, the candidate must have been called to the Bar. Also for admission into the school of Business and Human Resource Management Postgraduate Diploma programmes, candidates with professional qualifications such as ACA, ACIS, ACIB, ACII, ACIA, etc may be considered on individual merit.<br />
    For Master degree programmes, candidates must possess either a Bachelors Degree with a minimum of second class lower in the relevant field of study from a University recongnised by the Senate of the NOUN. However, holders of lower classification and with a minimum of 2 years of relevant post-qualification experience may be considered on individual merit OR a Postgraduate Diploma in related field of study from a recongnised University OR A Higher National Diploma with a minimum of Upper Credit in the relevant field of study. Candidate with membership certificate of relevant professional bodies may apply for the Master of Business (MBA) programme.<br />
    For the Doctor of Philosophy Degree prorammes, candidates must possess a masters degree in relevant field of study from a University recongised by the Senate of the NOUN with a minimum score average of not less than 60% or a cumulative Grade Point Average (CGPA) of 3.5 on a 5-point scale. Studying with us gives a rewarding experience as we give you all the support you need to succeed. You are welcome.<br /><br /><br />

