	Learning at a distance<br />

Studying at NOUN gives you the unique opportunity to study and obtain job/career enhancing degree and certificates at your own pace.<br />

It is however, important for you to note that this method of learning requires new approaches to studying.<br /><br />

Tips About Distance Learning<br /><br />

    It will require more self –discipline and responsibility you as you will study at your own pace.<br />
    Excellent time management skill will be needed to keep up with the pace of work.<br />
    Study every day because it enables you to spread your work out over a period of time than to attempt to cover too much within a short space time.<br />
    Set realistic goals for daily studies and keep to them.<br />
    Develop a weekly study plan. Review your work at the end of each week and modify your plan accordingly.<br />
    Use a calendar/diary to plan as much as possible.<br /><br />

Hints On How To Study Your Courses<br /><br />

    Be focused on your academic pursuit.<br />
    Carry out all activities recommended.<br />
    Form self –help study groups with other known NOUN students within your locality, as this will help spur you on to achieve your goals.<br /><br />

Examination tips<br /><br />

    Prepared in good time (at least 3 weeks ahead of exam date).<br />
    Revise your notes and review what you read with your study group.<br />
    Revise past examination question.<br />
    Cross check and confirm examination time table and venue in advance.<br /><br />

Writing Examinations, Assignments and Project Reports<br /><br />

    Research and gather information before writing out in full.<br />
    Organise your information and make notes.<br />
    Remember to number your pages.<br />
    Make outline and draft of your proposed essay/write –up.<br />
    Revise and finalise your work.<br /><br />

References<br /><br />

    Always acknowledge your references in your essay/write –up.<br />
    Beware of copyright issues which can also be legal issues.<br />
    Be creative, but when you do need to copy verbatim you must acknowledge your sources of information<br />

Tutorials<br /><br />

    You will have the opportunity to attend tutorials at your study Centre.<br />
    Attendance at tutorials is, however, optional.<br /><br />

