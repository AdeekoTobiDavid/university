    Click “Pay”<br/>
    Copy and take your RRR number to any bank branch for payment.<br/><br/><br/>

After Payment<br/><br/><br/>

    Login<br/>
    Goto  “Manage Wallet”<br/>
    Click “Check Payment Status”<br/>
    Enter Matric Number and RRR number<br/>
    Click “Check Wallet Payment”<br/><br/><br/>

