<p>
	
	
The University Act of 1983 is the operating basis for the actualisation of the re-born  NOUN.<br /> 
The Act provides, amongst others, that the  President of the  Federal Republic of Nigeria is the  Visitor to  the university.<br />  
This is similar to the custom in the conventional universities in the country. In addition, there is a Council headed by the Pro-Chancellor, whose members are constituted by the Visitor through the Honourable Minister of Education.<br /> 
 Currently, the Council of NOUN is made up of 15 appointed members apart from the internal  members of  the university  and representatives of  designated agencies  of government.<br />  
 The academic activities of the university are run through the Senate which has the  Vice-Chancellor as its Chairman and all Professors and Programme Leaders as members.<br /> 
