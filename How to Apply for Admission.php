Step by Step Procedure on How to Apply for Admission<br /><br />

    Visit the site www.nouonline.net<br />
    On the Home Page, go to the Menu Bar, Click on Apply for Admission<br />
    Select either Undergraduate Programme or Postgraduate Programme. Other programme is not available at the moment<br />
    Goto “Choose Faculty” (note it can be found at the left hand side of the screen)<br />
    Select Faculty<br />
    Fill displayed form<br />
    Click below to view admission criteria<br />
    Click “Apply” to proceed.<br />
    Take note of the UNIQUE ID displayed<br />
    Click 'Continue' button<br />
    Select Bank Branch as payment type<br />
    Click “Pay”<br />
    Copy and take your RRR number to any bank branch for payment.<br /><br />

After payment<br /><br />

    Perform step 1 to 3<br />
    Goto “Continue After Payment”<br />
    Input Unique ID, RRR number and Programme<br />
    Click “Proceed”<br />
    Fill Form.  Note: all fields marked with red star are compulsory.<br /><br />

After filling form<br /><br />

    Click “Submit”<br />
    Print Admission Letter<br />
    Visit the nearest study centre for screening<br /><br /><br />












		
